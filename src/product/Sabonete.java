package product;

public class Sabonete {

	private String tipo;
	private int massa;
	private int volume;
	private String piramideOufativa;
	
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public int getMassa() {
		return massa;
	}
	public void setMassa(int massa) {
		this.massa = massa;
	}
	public int getVolume() {
		return volume;
	}
	public void setVolume(int volume) {
		this.volume = volume;
	}
	public String getPiramideOufativa() {
		return piramideOufativa;
	}
	public void setPiramideOufativa(String piramideOufativa) {
		this.piramideOufativa = piramideOufativa;
	}
	
	
}
